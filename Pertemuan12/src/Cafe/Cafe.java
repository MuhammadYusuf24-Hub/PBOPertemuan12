    private void dupdate1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dupdate1ActionPerformed
        // TODO add your handling code here:
        try {
            // Ambil data dari TextField di dialog
            int idPesanan = Integer.parseInt(tid4.getText().trim());
            String namaPelanggan = tnama4.getText().trim();
            int jumlah = Integer.parseInt(tjumlah4.getText().trim());
            int idMenuInput = Integer.parseInt(tmen4.getText().trim());

            // Validasi field kosong
            if (namaPelanggan.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nama pelanggan harus diisi!");
                return;
            }

            // Buat EntityManager
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("Pertemuan12PU");
            EntityManager em = emf.createEntityManager();

            em.getTransaction().begin();

            // Cari objek Pesanan
            Pesanan pesan = em.find(Pesanan.class, idPesanan);
            if (pesan == null) {
                JOptionPane.showMessageDialog(this, "Data Pesanan tidak ditemukan!");
                em.getTransaction().rollback();
                em.close();
                emf.close();
                return;
            }

            // Cari Menu berdasarkan ID input
            Menu menuObj = em.find(Menu.class, idMenuInput);
            if (menuObj == null) {
                JOptionPane.showMessageDialog(this, "Menu dengan ID " + idMenuInput + " tidak ditemukan!");
                em.getTransaction().rollback();
                em.close();
                emf.close();
                return;
            }

            // Update data
            pesan.setNamaPelanggan(namaPelanggan);
            pesan.setJumlah(jumlah);
            pesan.setIdMenu(menuObj);

            em.merge(pesan); // simpan perubahan
            em.getTransaction().commit();
            em.clear(); // pastikan data terbaru terlihat

            // Refresh JTable di GUI utama
            UpdateDialog1.setVisible(false);
            tampilData2();
            UpdateDialog.setVisible(false);
            clear(); // kosongkan field dialog

            JOptionPane.showMessageDialog(this, "Data berhasil diupdate!");

            em.close();
            emf.close();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Jumlah Pesanan dan ID Menu harus berupa angka!");
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(this, "Kesalahan update: " + e.getMessage());
        }
    }//GEN-LAST:event_dupdate1ActionPerformed

