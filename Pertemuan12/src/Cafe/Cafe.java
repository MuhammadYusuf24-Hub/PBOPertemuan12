    private void tampilData1() {
        // Membuat koneksi EntityManager dari persistence unit
        EntityManager em = Persistence.createEntityManagerFactory("Pertemuan12PU").createEntityManager();
        try {
            Query q = em.createNamedQuery("Menu.findByIdMenu");
            List<Menu> hasil = q.getResultList();

            // Membuat model tabel baru dengan kolom yang tidak bisa diedit
            DefaultTableModel model = new DefaultTableModel(new String[]{"ID Menu", "Nama Menu", "Harga", "Kategori"}, 0) {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false; // semua kolom tidak bisa diedit langsung
                }
            };
           
            for (Menu data : hasil) {
                Object[] baris = new Object[5];
                baris[0] = data.getIdMenu();
                baris[1] = data.getNamaMenu();
                baris[2] = data.getHarga();
                baris[3] = data.getKategori();
                model.addRow(baris);
            }
            
            jtab1.setModel(model);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menampilkan data: " + e.getMessage());
        } finally {
            em.close(); 
        }
    }

    private void tampilData2() {
        EntityManager em = Persistence.createEntityManagerFactory("Pertemuan12PU").createEntityManager();

        try {
            // Ambil semua data Pesanan, urut berdasarkan ID
            List<Pesanan> hasil = em.createNamedQuery("Pesanan.findAll", Pesanan.class)
                    .getResultList();

            DefaultTableModel model = new DefaultTableModel(
                    new String[]{"ID Pesanan", "Nama Pelanggan", "Jumlah Pesanan", "ID Menu"}, 0
            ) {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };

            for (Pesanan data : hasil) {
                Object[] baris = new Object[4];
                baris[0] = data.getIdPesanan();
                baris[1] = data.getNamaPelanggan();
                baris[2] = data.getJumlah();
                // Tampilkan ID Menu, bisa juga ganti getNamaMenu() jika ingin nama
                baris[3] = data.getIdMenu() != null ? data.getIdMenu().getIdMenu() : "-";
                model.addRow(baris);
            }

            jtab2.setModel(model);
            jtab2.revalidate();
            jtab2.repaint();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menampilkan data: " + e.getMessage());
        } finally {
            em.close();
        }
    }

