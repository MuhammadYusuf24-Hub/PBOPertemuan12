    private void bupload2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bupload2ActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        int result = fileChooser.showOpenDialog(this);

        if (result == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();

            // Buat EntityManagerFactory sekali
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("Pertemuan12PU");
            EntityManager em = emf.createEntityManager();

            try (BufferedReader br = new BufferedReader(new FileReader(file))) {
                em.getTransaction().begin();

                String line;
                int count = 0;

                while ((line = br.readLine()) != null) {
                    line = line.trim();
                    if (line.isEmpty() || line.startsWith("idPesanan")) {
                        continue; // lewati header atau baris kosong
                    }
                    String[] data = line.split(";");
                    if (data.length != 4) {
                        System.out.println("Format salah: " + line);
                        continue;
                    }

                    try {
                        int idPesanan = Integer.parseInt(data[0].trim());
                        String namaPelanggan = data[1].trim();
                        int jumlah = Integer.parseInt(data[2].trim());
                        int idMenu = Integer.parseInt(data[3].trim());

                        // Ambil Menu dari DB
                        Menu menuObj = em.find(Menu.class, idMenu);
                        if (menuObj == null) {
                            System.out.println("Menu dengan ID " + idMenu + " tidak ditemukan, baris dilewati.");
                            continue;
                        }

                        // Buat objek Pesanan
                        Pesanan p = new Pesanan();
                        p.setIdPesanan(idPesanan);
                        p.setNamaPelanggan(namaPelanggan);
                        p.setJumlah(jumlah);
                        p.setIdMenu(menuObj);

                        em.persist(p); // simpan ke database
                        count++;

                    } catch (NumberFormatException e) {
                        System.out.println("Baris dilewati (angka salah): " + line);
                    }
                }

                em.getTransaction().commit();
                em.clear(); // hapus cache persistence
                tampilData2(); // refresh JTable

                JOptionPane.showMessageDialog(this,
                        "Upload berhasil! " + count + " data dimasukkan.",
                        "Sukses", JOptionPane.INFORMATION_MESSAGE);

            } catch (IOException e) {
                JOptionPane.showMessageDialog(this, "Kesalahan saat membaca file: " + e.getMessage());
                if (em.getTransaction().isActive()) {
                    em.getTransaction().rollback();
                }
            } catch (Exception e) {
                if (em.getTransaction().isActive()) {
                    em.getTransaction().rollback();
                }
                JOptionPane.showMessageDialog(this, "Kesalahan upload: " + e.getMessage());
            } finally {
                em.close();
                emf.close();
            }

        } else {
            JOptionPane.showMessageDialog(this, "Upload dibatalkan.", "Informasi", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_bupload2ActionPerformed

