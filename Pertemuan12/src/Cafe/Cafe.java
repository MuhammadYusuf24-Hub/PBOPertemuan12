    private void dyaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dyaActionPerformed
        // TODO add your handling code here:
        try {
            int id = Integer.parseInt(tid2.getText());

            EntityManagerFactory emf = Persistence.createEntityManagerFactory("Pertemuan12PU");
            EntityManager em = emf.createEntityManager();

            em.getTransaction().begin();
            Menu m = em.find(Menu.class, id);

            if (m != null) {
                try {
                    em.remove(m);
                    em.getTransaction().commit();

                    DeleteDialog.setVisible(false);
                    tampilData1();
                    clear();

                    JOptionPane.showMessageDialog(this,
                            "Data berhasil dihapus!",
                            "Sukses", JOptionPane.INFORMATION_MESSAGE);

                } catch (RollbackException ex) {
                    // Ambil root cause paling dalam (biasanya PSQLException)
                    Throwable cause = ex;
                    while (cause.getCause() != null) {
                        cause = cause.getCause();
                    }

                    String message = cause.getMessage();

                    // Tangani error FK constraint dari PostgreSQL
                    if (message != null && message.contains("violates foreign key constraint")) {
                        DeleteDialog.setVisible(false);

                        JOptionPane.showMessageDialog(this,
                                "Tidak dapat menghapus data karena masih digunakan di tabel lain",
                                "Gagal Menghapus", JOptionPane.WARNING_MESSAGE);

                    } else {
                        JOptionPane.showMessageDialog(this,
                                "️Terjadi kesalahan saat menghapus: " + message,
                                "Error", JOptionPane.ERROR_MESSAGE);
                    }

                    if (em.getTransaction().isActive()) {
                        em.getTransaction().rollback();
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this,
                        "Data tidak ditemukan!",
                        "Info", JOptionPane.INFORMATION_MESSAGE);
            }

            em.close();
            emf.close();

        } catch (HeadlessException | NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                    "️Kesalahan input: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_dyaActionPerformed

